n = int(input())
hoitsu = {1:0, 2:1, 3:1}

for i in range(4, n+1):
    chaeso = []

    if i%2 == 0:
        chaeso.append(hoitsu[i//2]+1)
    if i%3 == 0:
        chaeso.append(hoitsu[i//3]+1)
    chaeso.append(hoitsu[i-1]+1)
    hoitsu[i] = min(chaeso)
        
print(hoitsu[n])